    private void btn_cetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cetakActionPerformed
//  Menyimpan Slip Gaji ke file .txt
        PrintWriter out = null;
        try {
            // TODO add your handling code here:
            out = new PrintWriter(new BufferedWriter(new FileWriter(new File("slip_gaji",String.valueOf(cb_pilih_nama.getSelectedItem())+".txt"))));
            out.write(txt_slip.getText());
            out.close();
            
            
//  Membuka Notepad dan open file berdasarkan nama
            String dir = String.valueOf(cb_pilih_nama.getSelectedItem()+".txt");
            ProcessBuilder pb = new ProcessBuilder("Notepad.exe", "slip_gaji/" +dir);
            pb.start();
                 
        } catch (IOException ex) {
            Logger.getLogger(Home_UI.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            out.close();
        }
    }//GEN-LAST:event_btn_cetakActionPerformed

    
