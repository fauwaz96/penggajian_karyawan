//  Mengambil variabel pendidikan berdasarkan radio box
    public String pilih_pendidikan(){
        String pendidikan = null;
        
        if (pend_sma.isSelected() == true){
            pendidikan = "SMA";
        }
        else if (pend_d3.isSelected() == true){
            pendidikan = "D3";
        }
        else if (pend_s1.isSelected() == true){
            pendidikan = "S1";
        }
        return pendidikan;
    }
    
//  Mengambil variabel kelamin berdasrkan radio box 
    public String pilih_kelamin(){
        String kelamin = null;
        
        if (rad_laki_laki.isSelected() == true){
            kelamin = "Laki-laki";         
        }
        else if (rad_perempuan.isSelected() == true){
            kelamin = "Perempuan";          
        }
        return kelamin;
    }
    
//  mengambil variabel sisa cuti berdasarkan pilihan kelamin
    public Integer sisa_cuti(){
        int cuti_sisa = 0;
        
        if ("Laki-laki".equals(this.pilih_kelamin())){
            cuti_sisa = 5;
        }
        else if ("Perempuan".equals(this.pilih_kelamin())){
            cuti_sisa = 30;
        }
        return cuti_sisa;
    }

