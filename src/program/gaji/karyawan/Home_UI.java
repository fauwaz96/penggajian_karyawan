//  Menampilkan Tabel Karyawan
    public void tampil_tabel(){
        DefaultTableModel tb = new DefaultTableModel();
        tb.addColumn("Nama");
        tb.addColumn("Pendidikan");
        tb.addColumn("Kelamin");
        tb.addColumn("Sisa Cuti");
        tb.addColumn("Tanggal");
        jTable1.setModel(tb);
        tb.fireTableDataChanged();
      
        try{
            rst=stm.executeQuery("select * from karyawan");
            while (rst.next()){
                tb.addRow(new Object[]{
                rst.getString("nama"),
                rst.getString("pendidikan"),
                rst.getString("kelamin"),
                rst.getString("sisa_cuti"),
                rst.getString("tanggal"),
                });
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Home_UI.class.getName()).log(Level.SEVERE, null, ex);
        }  
    }
    
    
    
